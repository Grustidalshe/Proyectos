#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*last;

	last = *lst;
	if (!last)
	{
		*lst = new;
		return ;
	}
	last = ft_lstlast(last);
	last -> next = new;
}
